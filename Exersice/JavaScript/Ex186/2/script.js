
function checkSign(num) {
    if (num > 0) {
        console.log('+++');
    } else if (num < 0) {
        console.log('---');
    } else {
        console.log('The number is zero.');
    }
}

// Проверим работу функции
checkSign(5); // выведет '+++'
checkSign(-7); // выведет '---'
checkSign(0); // выведет 'The number is zero.'
