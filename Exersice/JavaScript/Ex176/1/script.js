
// Заданная строка
let str = 'http://www.example.com';

// Проверка, начинается ли строка на 'http://'
let startsWithHttp = str.startsWith('http://');
console.log(`Строка начинается с 'http://': ${startsWithHttp}`);

// Проверка, заканчивается ли строка на '.html'
let endsWithHtml = str.endsWith('.html');
console.log(`Строка заканчивается на '.html': ${endsWithHtml}`);

