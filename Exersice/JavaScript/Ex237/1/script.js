// Обычные функции
let func1 = function (num1, num2) {
    let result = num1 * num2;
    return result;
}

let func2 = function (num) {
    return num * num;
}

let func3 = function () {
    console.log('!!!');
}

// Стрелочные функции
let arrowFunc1 = (num1, num2) => {
    let result = num1 * num2;
    return result;
}

let arrowFunc2 = num => num * num;

let arrowFunc3 = () => console.log('!!!');

