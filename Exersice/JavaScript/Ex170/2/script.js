let squareRoot2 = Math.sqrt(587);

// Округление в большую сторону (ceil)
let roundedUp = Math.ceil(squareRoot2);

// Округление в меньшую сторону (floor)
let roundedDown = Math.floor(squareRoot2);

let roundingResults = {
    'floor': roundedDown,
    'ceil': roundedUp
};

console.log(roundingResults);
