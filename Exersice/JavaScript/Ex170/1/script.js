let squareRoot = Math.sqrt(379);

// Округление до целых
let roundedInteger = Math.round(squareRoot);

// Округление до десятых
let roundedTenth = squareRoot.toFixed(1);

// Округление до сотых
let roundedHundredth = squareRoot.toFixed(2);

console.log(`Rounded to Integer: ${roundedInteger}`);
console.log(`Rounded to Tenth: ${roundedTenth}`);
console.log(`Rounded to Hundredth: ${roundedHundredth}`);



